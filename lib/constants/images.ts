/**
 * Kategoriye göre görsel al (her zaman ilk görsel - sabit)
 */
export function getRandomImage(category: keyof typeof FINANCE_IMAGES): string {
  const images = FINANCE_IMAGES[category]
  if (Array.isArray(images)) {
    return images[0] // Her zaman ilk görsel
  }
  return images
}

/**
 * Kategori slug'ına göre görsel al
 */
export function getImageByCategory(categorySlug: string): string {
  const mapping: Record<string, keyof typeof FINANCE_IMAGES> = {
    'forex': 'forex',
    'crypto': 'crypto',
    'cryptocurrency': 'crypto',
    'stock-market': 'stockMarket',
    'stocks': 'stockMarket',
    'commodities': 'commodities',
    'gold': 'commodities',
    'education': 'education',
    'learning': 'education',
  }

  const category = mapping[categorySlug] || 'forex'
  return getRandomImage(category)
}

/**
 * Article için görsel öner
 */
export function suggestArticleImage(title: string, category?: string): string {
  const lowerTitle = title.toLowerCase()
  
  // Title'a göre akıllı öneri
  if (lowerTitle.includes('bitcoin') || lowerTitle.includes('btc')) {
    return getRandomImage('crypto')
  }
  if (lowerTitle.includes('gold') || lowerTitle.includes('silver')) {
    return getRandomImage('commodities')
  }
  if (lowerTitle.includes('eur') || lowerTitle.includes('gbp') || lowerTitle.includes('forex')) {
    return getRandomImage('forex')
  }
  if (lowerTitle.includes('stock') || lowerTitle.includes('s&p')) {
    return getRandomImage('stockMarket')
  }
  if (lowerTitle.includes('learn') || lowerTitle.includes('guide')) {
    return getRandomImage('education')
  }

  // Category'e göre
  if (category) {
    return getImageByCategory(category)
  }

  // Fallback
  return getRandomImage('forex')
}

